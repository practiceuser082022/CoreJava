package com.orangehrm;

public class OperatorsDemo1 {

	public static void main(String[] args) {
	
		// +, - , * , / , %
		
		int x=10, y=20, z;
		
		z= y + x;
		
		System.out.println("z value after addition "+ z);
		
		z= y-x;
		
		System.out.println("z value after subtraction "+ z);
		
		z= y * x;
		
		System.out.println("z value after multiply "+ z);
		
		z= y / x;
		
		System.out.println("z value after division "+ z);
		
		z= y % x;
		
		System.out.println("z value after modulo "+ z);
	}

}
