package com.orangehrm;

public class OperatorsDemo2 {

	public static void main(String[] args) {
		
		
		// short hand operators : +=,-=,*=,/=,%=
		
		int x=0;
		
		x+=20; // x= x + 20;
		System.out.println(x); // 20
		
		x-=10; // x= x - 10;
		System.out.println(x); // 10
		
		x*=2;  // x= x * 2;
		System.out.println(x); // 20 
		
		x/=10 ; // x= x / 10;
		System.out.println(x); // 2 
		
		x%=2;   // x = x % 2;
		System.out.println(x); // 0
		
	     

	}

}
