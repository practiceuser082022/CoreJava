package com.orangehrm;

public class OperatorsDemo3 {

	public static void main(String[] args) {
	
		int x=20;
		
		// ++ , -- : increment, decrement operator
		
		// x++ -> x=x+1;
		// x-- -> x=x-1;
		
		
		x++; // post increment
		System.out.println(x);
		
		++x; // pre increment
		System.out.println(x++);
		
		x--; // post decrement
		System.out.println(x);
		
		--x; // pre decrement
		System.out.println(x);

	}

}
